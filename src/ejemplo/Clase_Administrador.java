public class Clase_Administrador implements Interface_Imprimible{
    private String usuario;
    private String pass;

    public Clase_Administrador(String usuario, String pass) {
        this.usuario = usuario;
        this.pass = pass;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    @Override
    public String Imprimir() {
       return this.usuario;
    }
    
    
    
}
