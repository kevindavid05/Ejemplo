public class Clase_usuario implements Interface_Imprimible{
    private String rut;
    private String nombre;

    public Clase_usuario(String rut, String nombre) {
        this.rut = rut;
        this.nombre = nombre;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String Imprimir() {
       return this.rut + " " + this.nombre;
    }
    
    
    
}
